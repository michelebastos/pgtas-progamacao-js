let voltasAteSatisfeito = 5;
let qtdeVoltasAtual = 0;

while (qtdeVoltasAtual <= voltasAteSatisfeito) {
  console.log(`Volta na quadra nº ${qtdeVoltasAtual}`);
  qtdeVoltasAtual++;
}
